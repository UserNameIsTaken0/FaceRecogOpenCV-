    public void pagWeb(String url) {
        String osName = System.getProperty("os.name");
        try {
            if (osName.startsWith("Windows")) {
                Runtime.getRuntime().exec("rundll32 url.dll,FileProtocolHandler " + url);
            } else if (osName.startsWith("Mac OS X")) {
                Runtime.getRuntime().exec("open " + url);
            } else {
                System.out.println("Please open a browser and go to " + url);
            }
        } catch (IOException e) {
            System.out.println("Failed to start a browser to open the url " + url);
            e.printStackTrace();
        }
    }

    public void setAll(Estudiante UL) {
        //Se establecen todos los campos respecto al usuario
        Nom.setText(UL.nombre + " " + UL.apellidoP + " " + UL.apellidoM);
        nctrl.setText("" + UL.getNoctrl());
        ca.setText("" + UL.getCreditos());
        //Los creditos faltantes es un math random, simplemente por agilizar
        falt.setText("" + (Math.floor((Math.random() * 10) + 30) - UL.getCreditos()));
        sem.setText("" + UL.getSemestre());
        //Se empiesan a cargar datos a las tablas, cada tabla tiene su modelo porque esta pc me odia
        //y si reutilizaba el modelo, deudas queda inutilizado.
        DefaultTableModel modelo = (DefaultTableModel) adeudo.getModel();
        modelo.addRow(UL.getAdeudos());
        adeudo.setModel(modelo);
        String[][] tmp = UL.getHorario();
        DefaultTableModel modelo1 = (DefaultTableModel) Horario.getModel();
        if (tmp != null) {
            for (String[] horario : UL.getHorario()) {
                modelo1.addRow(horario);
            }

        }

        Horario.setModel(modelo1);
    }

